
class Product {


    constructor(id_product, name, category, description, price, color, size, 
        composition, garment_care, image_main, stock){

            this.id_product = id_product;
            this.name = name;
            this.category = category;
            this.description = description;
            this.price = price;
            this.color = color;
            this.size = size;
            this.composition = composition;
            this.garment_care = garment_care;
            this.image_main = image_main;
            this.stock = stock;

        }

        
}




